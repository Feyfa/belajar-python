# list -> array, mengakses data dengan menggunakan index

data_list = ['jidan', 'feyfa', 'fena']
print(data_list[0])

# dictionary (dic) -> associative array
# identifier -> key

data_dict = {
    'jd': 'jidan',
    'fy': 'feyfa',
    'fn': 'fena',
    'number': 1000,
    'list': data_list
}

print(data_dict['jd'])
print(data_dict['fy'])
print(data_dict['fn'])
print(data_dict['number'])
print(data_dict['list'])