# operasi yang dapat dilakukan penyingkatan
a = 5

a += 5 # ini artinya a = a + 5
print(f'a = {a}')

a -= 5 # ini artinya a = a - 5
print(f'a = {a}')

a *= 5 # ini artinya a = a * 5
print(f'a = {a}')

a /= 5 # ini artinya a = a / 5
print(f'a = {a}')


b = 8
b //= 3 # ini artinya b = b // 3, pembagian yang dibulatkan ke bawah
print(f'b = {b}')

c = 3
c **= 4 # ini artinya adalah c = c ** 4, c ^ 4
print(f'c = {c}')
